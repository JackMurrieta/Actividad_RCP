package Inventario;

/**
 *
 * @author Jack Murrieta
 */
public class ProductoInventario {

    private String id;
    private String nombre;
    private double precio;
    private int stock;

    public ProductoInventario(String id, String nombre, double precio, int stock) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.stock = stock;
    }

    public String getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
}
